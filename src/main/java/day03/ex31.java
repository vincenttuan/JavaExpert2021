package day03;

public class ex31 {

    public static void main(String[] args) {
        // 程式中的迴圈 (loop) 就是在特定程式區塊 (block) 中，重複執行相同的工作
        // 避免造成無窮迴圈 (不會結束)

        // 輸出 54321
        int i;
        for (i = 5; i >= 1 ; i--) {
            System.out.print(i);
        }
        System.out.print('\n'); //換行

    }
}
