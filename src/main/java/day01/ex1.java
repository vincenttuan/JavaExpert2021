        // 字元 char
        System.out.println('a');
        // 字串 String
        System.out.println("Hello Java! 字串");
        // 布林值 boolean (true / false)
        System.out.println(3 < 5);
        // 跳脫字元
        System.out.println("Hello\t\"\\Java!\"");
